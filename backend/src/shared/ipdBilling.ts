// Drugs are matched by name (case-insensitive) because the legacy
// Prescription.drugs JSON only stores names, not Drug.id. If multiple
// SKUs share the same display name (different strengths/forms), the
// first active row wins — operators should keep master data clean for
// the rest. Hospitals that want strict id-based matching can update
// the prescription create flow to pass drug ids.
export async function requireDrugPriceByName(tx: TxClient | typeof prisma, drugName: string): Promise<{ id: string; name: string; price: number }> {
  if (!drugName || !drugName.trim()) throw new MissingPriceError('drug', '(empty name)');
  const row = await tx.drug.findFirst({
    where: { name: { equals: drugName.trim(), mode: 'insensitive' }, isActive: true },
    select: { id: true, name: true, price: true },
  });
  if (!row || Number(row.price) <= 0) throw new MissingPriceError('drug', drugName);
  return { id: row.id, name: row.name, price: Number(row.price) };
}

// Gets the open IPD invoice for an admission's encounter, or creates
// an empty draft if none exists. Encounter-scoped (not admission-
// scoped) because Invoice has FKs to Encounter, not Admission, and the
// IPD admission always has exactly one Encounter (1-to-1).
export async function getOrCreateActiveIpdInvoice(
  tx: TxClient,
  args: { patientId: string; encounterId: string },
) {
  // Open invoice = same encounter, type='ipd', and not yet settled.
  // 'paid' is a terminal state; 'cancelled' likewise. Anything else
  // (draft, final, partial, overpaid) is still in play.
  const existing = await tx.invoice.findFirst({
    where: {
      encounterId: args.encounterId,
      type: 'ipd',
      status: { notIn: ['paid', 'cancelled'] },
    },
  });
  if (existing) return existing;

  return tx.invoice.create({
    data: {
      patientId: args.patientId,
      encounterId: args.encounterId,
      type: 'ipd',
      items: [],
      subtotal: 0,
      discount: 0,
      tax: 0,
      total: 0,
      paid: 0,
      balance: 0,
      status: 'draft',
    },
  });
}

// Appends one or more line items to an invoice and recomputes the
// money columns. The existing `discount` is preserved as an absolute
// amount (matching the discount endpoint contract); tax is recomputed
// only if there's an existing taxPercent stored — for the current
// schema there's no tax-percent column, so tax stays as-is.
export async function appendLineItems(
  tx: TxClient,
  invoiceId: string,
  newItems: LineItem[],
): Promise<void> {
  if (!newItems.length) return;

  const inv = await tx.invoice.findUnique({ where: { id: invoiceId } });
  if (!inv) throw new Error(`Invoice ${invoiceId} not found`);

  const currentItems = Array.isArray(inv.items) ? (inv.items as unknown as LineItem[]) : [];
  // De-dupe by (sourceType, sourceId, id) — if the caller retries an
  // order create, the second pass shouldn't double-bill. The check is
  // belt-and-braces; the upstream transactional boundary should
  // already prevent this in normal operation.
  const existingKeys = new Set(
    currentItems.map((it) => `${it.sourceType || ''}::${it.sourceId || ''}::${it.id}`),
  );
  const filteredNew = newItems.filter(
    (it) => !existingKeys.has(`${it.sourceType || ''}::${it.sourceId || ''}::${it.id}`),
  );
  if (!filteredNew.length) return;

  const mergedItems = [...currentItems, ...filteredNew];

  const newSubtotal = +mergedItems.reduce((s, it) => s + Number(it.total || 0), 0).toFixed(2);
  const discount = Number(inv.discount || 0);
  const tax = Number(inv.tax || 0);
  const newTotal = +(newSubtotal + tax - discount).toFixed(2);
  const paid = Number(inv.paid || 0);
  const newBalance = +(newTotal - paid).toFixed(2);

  await tx.invoice.update({
    where: { id: invoiceId },
    data: {
      items: mergedItems as unknown as Prisma.InputJsonValue,
      subtotal: newSubtotal,
      total: newTotal,
      balance: newBalance,
      // Bump 'draft' to 'final' once there's a real line on the bill.
      // 'paid'/'cancelled' wouldn't have been returned by
      // getOrCreateActiveIpdInvoice so we don't need to guard them.
      status: inv.status === 'draft' ? 'draft' : inv.status,
    },
  });
}

// Convenience wrapper: idempotently bill a batch of lab tests for a
// patient if they're currently admitted. Returns the line items added
// (empty array if the patient isn't admitted — auto-bill only applies
// to IPD). Throws MissingPriceError on a per-item basis so the caller
// can abort the whole order.
export async function autoBillLabTests(
  tx: TxClient,
  args: { tenantId: string; patientId: string; orderId: string; tests: Array<{ id?: string; code?: string; testId?: string; name?: string; quantity?: number }>; orderedAt: Date },
): Promise<LineItem[]> {
  const admission = await findActiveAdmission(tx, args.tenantId, args.patientId);
  if (!admission || !admission.encounterId) return [];

  const lines: LineItem[] = [];
  for (const t of args.tests) {
    const ref = t.id || t.code || t.testId || t.name || '';
    if (!ref) throw new MissingPriceError('lab_test', '(no id/code/name on test)');
    const priced = await requireLabTestPrice(tx, ref);
    const qty = Math.max(1, Number(t.quantity || 1));
    lines.push({
      id: `${args.orderId}-${priced.id}`,
      category: 'lab',
      description: priced.name,
      quantity: qty,
      unitPrice: priced.price,
      total: +(priced.price * qty).toFixed(2),
      sourceType: 'lab_order',
      sourceId: args.orderId,
      occurredAt: args.orderedAt.toISOString(),
    });
  }

  const invoice = await getOrCreateActiveIpdInvoice(tx, {
    patientId: args.patientId,
    encounterId: admission.encounterId,
  });
  await appendLineItems(tx, invoice.id, lines);
  return lines;
}

