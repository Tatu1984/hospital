// Prescription auto-billing for admitted patients. The drugs JSON shape
// is { name, dose?, frequency?, days?, quantity? } — we bill by name
// using Drug.price, multiplying by `quantity` if present, otherwise 1
// unit per drug entry. Ops can refine the quantity model later (e.g.
// derive from dose+days) without changing this helper's signature.
export async function autoBillPrescription(
  tx: TxClient,
  args: { tenantId: string; patientId: string; prescriptionId: string; drugs: Array<{ name: string; quantity?: number }>; issuedAt: Date },
): Promise<LineItem[]> {
  const admission = await findActiveAdmission(tx, args.tenantId, args.patientId);
  if (!admission || !admission.encounterId) return [];

  const lines: LineItem[] = [];
  for (const d of args.drugs) {
    const priced = await requireDrugPriceByName(tx, d.name);
    const qty = Math.max(1, Number(d.quantity || 1));
    lines.push({
      id: `${args.prescriptionId}-${priced.id}`,
      category: 'pharmacy',
      description: priced.name,
      quantity: qty,
      unitPrice: priced.price,
      total: +(priced.price * qty).toFixed(2),
      sourceType: 'prescription',
      sourceId: args.prescriptionId,
      occurredAt: args.issuedAt.toISOString(),
    });
  }

  const invoice = await getOrCreateActiveIpdInvoice(tx, {
    patientId: args.patientId,
    encounterId: admission.encounterId,
  });
  await appendLineItems(tx, invoice.id, lines);
  return lines;
}
