// Auto-itemised IPD billing helper.
//
// The hospital wants every clinical event for an admitted patient — lab
// orders, radiology orders, prescriptions, procedures — to land as a
// line item on the patient's running IPD invoice the moment it happens.
// Families can settle the bill in parts during the stay; on discharge
// the same invoice is finalised. Dialysis-only patients are NOT covered
// here: they pay per-session at OPD, so their workflow doesn't have an
// admission row to attach charges to.
//
// Design notes:
//
// 1. There is exactly ONE active IPD invoice per admission. The first
//    billable event creates it; every subsequent event appends a line
//    item and re-derives subtotal/total/balance, preserving any
//    discount that was already applied.
//
// 2. Pricing is fetched from the master tables (LabTestMaster,
//    RadiologyTestMaster, ProcedureMaster, Drug). If a price is
//    missing for any item, `requirePrice()` throws `MissingPriceError`
//    and the calling endpoint surfaces it as 422 — the user has chosen
//    to refuse orders that can't be priced rather than letting a ₹0
//    line slip through (master data is the source of truth).
//
// 3. Bed-day charges are NOT stored as line items here. They are
//    computed at fetch time in /api/ipd-billing/:admissionId so the
//    bed total stays current without a daily cron writing rows.
//
// 4. All append operations must run inside the same Prisma transaction
//    as the source row create (lab order, prescription, etc.) so a
//    crash between the source row and its line item can't leave the
//    invoice out of sync with the clinical record.

import { Prisma } from '@prisma/client';
import { prisma } from './prisma';

export class MissingPriceError extends Error {
  code = 'MISSING_PRICE';
  constructor(public itemType: string, public itemRef: string) {
    super(`No master-data price found for ${itemType} "${itemRef}"`);
    this.name = 'MissingPriceError';
  }
}

export interface LineItem {
  // Stable id within the items[] array — uuid-ish string the FE uses as
  // a key. Generated by the caller (lab order id + test code, etc.).
  id: string;
  category: 'lab' | 'radiology' | 'pathology' | 'pharmacy' | 'procedure' | 'consultation' | 'bed' | 'other';
  description: string;
  quantity: number;
  unitPrice: number;
  total: number;
  // Source row reference so the bill can link back to the order /
  // prescription / etc. Both fields are optional because some line
  // items (e.g. manual adjustments) won't have a source.
  sourceType?: string;
  sourceId?: string;
  // ISO timestamp of the event that produced this line.
  occurredAt: string;
}

type TxClient = Prisma.TransactionClient;

// Returns the active admission row for a patient, or null if the patient
// isn't currently admitted. Used both to decide whether to auto-bill and
// to attach the invoice to the right encounter.
//
// "Active" here means: no dischargeDate AND status in (active, admitted,
// in_progress). The schema doesn't enforce a strict enum, so we accept
// the historical variants we know about.
export async function findActiveAdmission(
  tx: TxClient | typeof prisma,
  tenantId: string,
  patientId: string,
) {
  return tx.admission.findFirst({
    where: {
      patientId,
      dischargeDate: null,
      status: { in: ['active', 'admitted', 'in_progress'] },
      patient: { tenantId },
    },
    select: { id: true, encounterId: true, patientId: true },
  });
}

// Resolves price for a billable item. Throws MissingPriceError if the
// master row is missing or has price <= 0 (a zero price is treated as
// "unknown" rather than "free" — explicit zero items should be flagged
// in master data with a different mechanism if ever needed).
export async function requireLabTestPrice(tx: TxClient | typeof prisma, testIdOrCode: string): Promise<{ id: string; name: string; price: number }> {
  const row = await tx.labTestMaster.findFirst({
    where: { OR: [{ id: testIdOrCode }, { code: testIdOrCode }] },
    select: { id: true, name: true, price: true },
  });
  if (!row || Number(row.price) <= 0) throw new MissingPriceError('lab_test', testIdOrCode);
  return { id: row.id, name: row.name, price: Number(row.price) };
}

